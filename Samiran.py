# Arithmetic operator

num1 = 10
num2 = 20
num3 = 3
num4 = 7

print("Num1 + Num2 =", num1 + num2)
print("Num1 - Num2 =", num1 - num2)
print("Num1 * Num2 =", num1 * num2)
print("Num2 / Num2 =", num2 / num1)
print("Num4 % Num3 =", num4 % num3)
print("Num4 // Num3 =", num4 // num3)
print("Num4 ^ Num3 =", num4 ** num3)

# Assignment operator

num5 = num1 + num2
print("Value =",num5)
num5 += num2                        # num5 = num5 + num2
print("Value_2=", num5)


